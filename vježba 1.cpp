#include <iostream>

int main() {
    // Ispisivanje imena i prezimena
    std::cout << "Vaše ime i prezime" << std::endl;

    // Prazan red
    std::cout << std::endl;

    // Ispisivanje brojeva od 1 do 9
    for (int i = 1; i <= 9; ++i) {
        std::cout << i << " ";
    }
    std::cout << std::endl;

    return 0;
}

